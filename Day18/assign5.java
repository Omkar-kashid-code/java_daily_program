class Demo{
	public static void main(String[]args){
	int arr[]={1,2,3,4,5,6};
		for(int i=0;i<arr.length;i++){
			if(arr[i]%2==0){
				System.out.println("even element "+arr[i]);
			}
			}
			for(int i=0;i<arr.length;i++){
			if(arr[i]%2==1){
				System.out.println("odd element "+arr[i]);
			}
			}
		
	}
}